nombre = 'Melissa Cuastuza'
#INDEXING
#Posición índice de 'M' es cero:
print('Indexing:')
print(nombre[0])
print(nombre[4])
#SLICING:
print('Slicing:')
print(nombre[1:4])
print(nombre[2:7])
#VALORES INTERCALADOS:
print('Intercalados:')
print(nombre[0:16:2])
print(nombre[0:])
print(nombre[0:16])
#REGRESIVO:
print('Regresivo:')
print(nombre[-16:-1])
print(nombre[-1:-9:-1])
print(nombre[-1::-1])
